package statestore

import "context"

// Store определяет механизм постоянного хранения состояния.
//
// Store используется для загрузки, сохранения и удаления
// значения произвольного типа.
//
// Конкретная реализация может хранить данные в файлах,
// базе данных, удаленном сервисе или любом другом хранилище.
type Store[T any] interface {
	// Load загружает значение из хранилища.
	//
	// Если состояние отсутствует, реализация должна вернуть
	// (nil, nil).
	//
	// Отсутствие состояния не считается ошибкой.
	Load(ctx context.Context) (*T, error)

	// Save сохраняет значение в хранилище.
	//
	// Сохраненное значение должно быть доступно
	// для последующего вызова Load.
	Save(ctx context.Context, value T) error

	// Destroy удаляет сохраненное состояние.
	//
	// После успешного вызова Destroy метод Load
	// должен возвращать (nil, nil).
	Destroy(ctx context.Context) error
}
