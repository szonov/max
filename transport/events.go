package transport

import (
	"encoding/json"

	"github.com/szonov/max/events"
)

// Events содержит события жизненного цикла и обмена данными транспорта.
//
// События Connect, Disconnect и Error вызываются из внутреннего цикла
// управления соединением.
//
// Событие Message доставляется асинхронно и не блокирует чтение
// WebSocket соединения.
type Events struct {
	// Connect вызывается после успешного установления соединения.
	//
	// Событие не содержит данных. Для получения информации о соединении
	// следует использовать состояние Transport.
	Connect events.Event

	// Disconnect вызывается при завершении соединения.
	//
	// В качестве параметра передаётся причина отключения.
	// Ошибка может быть nil, если соединение было закрыто штатно.
	Disconnect events.EventOf[error]

	// Error вызывается при возникновении ошибки внутри транспорта.
	//
	// Ошибка содержит информацию об операции, во время которой
	// произошёл сбой (dial, read, write, heartbeat).
	Error events.EventOf[error]

	// Message вызывается при получении нового сообщения.
	//
	// Данные передаются в исходном виде без разбора протокола.
	// Интерпретация сообщения должна выполняться уровнем выше
	// (например, RPC клиентом).
	//
	// Обработчики вызываются асинхронно, чтобы не блокировать цикл чтения
	// WebSocket соединения.
	//
	// Если обработчику необходимо выполнить длительную операцию или
	// отправить новый запрос через транспорт, он не блокирует получение
	// следующих сообщений.
	Message events.EventOf[json.RawMessage]
}
